// Helper function to get news item by slug
export function getNewsBySlug(slug) {
  return allNewsItems.find((item) => item.slug === slug) || null;
}

// Helper function to get related news based on tags and category
export function getRelatedNews(currentId, limit = 3) {
  const currentNews = allNewsItems.find((item) => item.id === currentId);
  if (!currentNews) return [];

  // Get news items that share tags or category with the current item
  const related = allNewsItems
    .filter(
      (item) =>
        item.id !== currentId &&
        (item.category === currentNews.category ||
          item.tags.some((tag) => currentNews.tags.includes(tag)))
    )
    .sort((a, b) => b.timestamp - a.timestamp) // Sort by newest first
    .slice(0, limit);

  return related;
}
